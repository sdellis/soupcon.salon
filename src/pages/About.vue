      <h2>Mutual Aid</h2>
      <p>
      Luckily, we do have some promising guides as we set out to create a better society.
      In 2020, Dean Spade published
      <a href="http://www.deanspade.net/mutual-aid-building-solidarity-during-this-crisis-and-the-next/">
      a book on practical organizing</a> called <em>Mutual Aid: Building Solidarity
      During This Crisis (And The Next)</em>. Spade defines "mutual aid" as collective coordination
      to meet each other's needs, usually from an awareness that the systems we have in place
      are not going to meet them. We see examples of mutual aid in every single social movement,
      whether it's raising money for workers on strike, putting drinking water in the desert for
      migrants crossing the border, or coordinating letter-writing to prisoners. These are mutual
      aid projects. They directly meet people's survival needs, and are based on a shared
      understanding that the conditions in which we are made to live are unjust.
      </p>
      <p>
      Today, many of us live in the most atomized societies in human history, which makes our
      lives less secure and undermines our ability to organize together to change unjust
      conditions on a larger scale. We are put into competition with each other for survival,
      and we are forced to rely on hostile systems for the things we need. In this context, mutual aid --
      where we choose to help each other out, share things, and put time and resources into caring
      for the most vulnerable -- is a radical act. It's how we build solidarity and enable real
      positive change to take root and grow.
      </p>
